import fs from "node:fs";
import path from "node:path";
import {
  normalizeTarget,
  readBundledTemplate,
  todayDate,
  toPosix,
} from "./core-shared.mjs";
import { splitMarkdownRow } from "./markdown-utils.mjs";
import { collectTasks } from "./task-scanner.mjs";
import {
  beginGovernanceSync,
  commitGovernanceSync,
  moduleGeneratedIndexSurfaces,
  releaseGovernanceSync,
} from "./governance-sync.mjs";
import { markdownCell } from "./task-lifecycle/text-utils.mjs";

export function rebuildGovernanceIndexes(targetInput, { dryRun = false, archive = false, apply = false } = {}) {
  const target = normalizeTarget(targetInput);
  const effectiveApply = Boolean(apply && !dryRun);
  const context = beginGovernanceSync(target, { operation: "governance rebuild", dryRun: !effectiveApply });
  try {
    const tasks = collectTasks(target)
      .filter((task) => task.deletionState !== "deleted")
      .sort((a, b) => String(a.id).localeCompare(String(b.id)));
    const surfaces = [...governanceSurfaces(target, tasks), ...moduleGeneratedIndexSurfaces(target, tasks)];
    const archiveDir = archive ? uniqueArchiveDir(target) : "";
    const changes = surfaces.map((surface) => ({
      surface: surface.surface,
      destination: surface.relative,
      action: apply ? "rebuild-governance-index" : "would-rebuild-governance-index",
      generatedRows: surface.rows.length,
      archive: archive ? `${archiveDir}/${surface.relative}` : "",
    }));

    if (!effectiveApply) {
      return { dryRun: true, archive, applied: false, archiveDir, changes };
    }

    const allowed = [];
    for (const surface of surfaces) {
      if (archive && fs.existsSync(surface.absolute)) {
        const archivePath = path.join(target.projectRoot, archiveDir, surface.relative);
        fs.mkdirSync(path.dirname(archivePath), { recursive: true });
        fs.copyFileSync(surface.absolute, archivePath);
        allowed.push(toPosix(path.relative(target.projectRoot, archivePath)));
      }
      fs.mkdirSync(path.dirname(surface.absolute), { recursive: true });
      fs.writeFileSync(surface.absolute, surface.content);
      allowed.push(surface.relative);
    }

    const commit = commitGovernanceSync(context, allowed, { message: "chore(harness): rebuild generated governance indexes" });
    return { dryRun: false, archive, applied: true, archiveDir, changes, commit };
  } finally {
    releaseGovernanceSync(context);
  }
}

function governanceSurfaces(target, tasks) {
  const featureTasks = tasks.filter((task) => !task.module);
  const moduleFeatureRows = moduleFeatureAggregateRows(tasks);
  const featureSurfaces = featureRegistryPaths(target).map((featurePath) => {
    const privateFeature = path.basename(featurePath) === "Private-Feature-SSoT.md";
    const rows = [
      ...featureTasks.map((task) => featureRow(task, privateFeature)),
      ...moduleFeatureRows.map((module) => moduleFeatureRow(module, privateFeature)),
    ];
    return {
      surface: privateFeature ? "private-feature-ssot" : "feature-ssot",
      absolute: featurePath,
      relative: toPosix(path.relative(target.projectRoot, featurePath)),
      rows,
      content: privateFeature
        ? renderPrivateFeatureRows(rows)
        : replaceTableRows(readBundledTemplate("templates/ssot/Feature-SSoT.md"), /^ID$/i, rows),
    };
  });
  return [
    ...featureSurfaces,
    {
      surface: "harness-ledger",
      absolute: path.join(target.docsRoot, "Harness-Ledger.md"),
      relative: toPosix(path.relative(target.projectRoot, path.join(target.docsRoot, "Harness-Ledger.md"))),
      rows: tasks.map(ledgerRow),
      content: replaceTableRows(readBundledTemplate("templates/ledger/Harness-Ledger.md"), /^ID$/i, tasks.map(ledgerRow)),
    },
  ];
}

function renderPrivateFeatureRows(rows) {
  return `# Private Feature SSoT

Generated by \`harness governance rebuild\`.

## Active Features

| ID | 状态 | Feature | 负责人 | 当前产物 | 备注 |
| --- | --- | --- | --- | --- | --- |
${rows.map((row) => `| ${fitRow(row, 6).join(" | ")} |`).join("\n")}
`;
}

function replaceTableRows(content, headerPattern, rows) {
  const lines = String(content || "").split(/\r?\n/);
  for (let index = 0; index < lines.length - 1; index += 1) {
    if (!lines[index].trim().startsWith("|")) continue;
    const header = splitMarkdownRow(lines[index]);
    if (!header.some((cell) => headerPattern.test(cell))) continue;
    const separator = splitMarkdownRow(lines[index + 1]);
    if (!separator.every((cell) => /^:?-{3,}:?$/.test(cell))) continue;
    let end = index + 2;
    while (end < lines.length && lines[end].trim().startsWith("|")) end += 1;
    const fitted = rows.map((row) => fitRow(row, header.length));
    lines.splice(index + 2, end - index - 2, ...fitted.map((row) => `| ${row.join(" | ")} |`));
    return `${lines.join("\n").trimEnd()}\n`;
  }
  return `${String(content || "").trimEnd()}\n\n${rows.map((row) => `| ${row.join(" | ")} |`).join("\n")}\n`;
}

function fitRow(row, length) {
  const next = row.map((cell) => markdownCell(cell));
  while (next.length < length) next.push("");
  return next.slice(0, length);
}

function featureRow(task, privateFeature) {
  const plan = stripTarget(task.taskPlanPath || `${stripTarget(task.path)}/task_plan.md`);
  if (privateFeature) {
    return [
      taskFeatureId(task, true),
      mapPrivateFeatureState(task.state),
      task.title || task.shortId || task.id,
      "coordinator",
      `\`${plan}\``,
      summary(task),
    ];
  }
  return [
    taskFeatureId(task, false),
    task.title || task.shortId || task.id,
    "CLI-generated task index",
    "coordinator",
    mapFeatureState(task.state),
    "P2",
    plan,
    task.materialsReady ? "materials-ready" : "materials-missing",
    task.reviewStatus || "pending",
    task.walkthroughPath ? stripTarget(task.walkthroughPath) : "pending",
    residual(task),
    todayDate(),
  ];
}

function moduleFeatureRow(module, privateFeature) {
  const plan = `docs/09-PLANNING/MODULES/${module.moduleKey}/module_plan.md`;
  if (privateFeature) {
    return [
      `PF-MODULE-${module.moduleKey}`,
      mapPrivateFeatureState(module.state),
      module.moduleKey,
      "coordinator",
      `\`${plan}\``,
      `grouped-index; tasks:${module.count}; queues:${module.queues.join(",") || "none"}`,
    ];
  }
  return [
    `F-MODULE-${module.moduleKey}`,
    module.moduleKey,
    "Grouped task index",
    "coordinator",
    mapFeatureState(module.state),
    "P2",
    plan,
    `task-list --module ${module.moduleKey}`,
    "module-index",
    "pending",
    module.residual,
    todayDate(),
  ];
}

function ledgerRow(task) {
  const plan = stripTarget(task.taskPlanPath || `${stripTarget(task.path)}/task_plan.md`);
  return [
    taskLedgerId(task),
    task.title || task.shortId || task.id,
    "coordinator",
    mapLedgerState(task.state),
    plan,
    task.module ? `F-MODULE-${task.module}` : taskFeatureId(task, false),
    task.materialsReady ? "materials-ready" : "materials-missing",
    task.reviewStatus === "confirmed" ? stripTarget(task.reviewPath) : (task.reviewStatus || "pending"),
    task.walkthroughPath ? stripTarget(task.walkthroughPath) : "pending",
    task.lessonCandidateDecisionComplete ? "checked" : (task.lessonCandidateStatus || "pending"),
    residual(task),
    todayDate(),
  ];
}

function moduleFeatureAggregateRows(tasks) {
  const grouped = new Map();
  for (const task of tasks) {
    if (!task.module) continue;
    if (!grouped.has(task.module)) grouped.set(task.module, []);
    grouped.get(task.module).push(task);
  }
  return [...grouped.entries()].sort(([a], [b]) => a.localeCompare(b)).map(([moduleKey, moduleTasks]) => ({
    moduleKey,
    count: moduleTasks.length,
    state: aggregateState(moduleTasks),
    queues: [...new Set(moduleTasks.flatMap((task) => task.taskQueues || []))].sort(),
    residual: moduleTasks.some((task) => !task.materialsReady) ? "module-materials-missing" : "none",
  }));
}

function aggregateState(tasks) {
  const states = new Set(tasks.map((task) => task.state));
  if (states.has("blocked")) return "blocked";
  if (states.has("review")) return "review";
  if (states.has("in_progress")) return "in_progress";
  if (tasks.length > 0 && tasks.every((task) => task.state === "done")) return "done";
  return "planned";
}

function summary(task) {
  const queues = Array.isArray(task.taskQueues) && task.taskQueues.length ? `queues:${task.taskQueues.join(",")}` : "queues:none";
  return `${queues}; review:${task.reviewStatus || "pending"}; materials:${task.materialsReady ? "ready" : "missing"}`;
}

function residual(task) {
  if (Array.isArray(task.stateConflicts) && task.stateConflicts.length) return `state-conflicts:${task.stateConflicts.length}`;
  if (Array.isArray(task.materialIssues) && task.materialIssues.length) return `material-issues:${task.materialIssues.length}`;
  return "none";
}

function featureRegistryPaths(target) {
  const privatePath = path.join(target.docsRoot, "09-PLANNING/Private-Feature-SSoT.md");
  const publicPath = path.join(target.docsRoot, "09-PLANNING/Feature-SSoT.md");
  const existing = [publicPath, privatePath].filter((filePath) => fs.existsSync(filePath));
  return existing.length > 0 ? existing : [publicPath];
}

function taskFeatureId(task, privateId) {
  return `${privateId ? "PF" : "F"}-${taskSlug(task)}`;
}

function taskLedgerId(task) {
  return `HL-${taskSlug(task)}`;
}

function taskSlug(task) {
  return String(task.shortId || task.id || "task").replace(/^TASKS\//, "").replace(/^MODULES\//, "").replace(/[^A-Za-z0-9-]+/g, "-").slice(0, 72);
}

function stripTarget(value) {
  return String(value || "").replace(/^TARGET:/, "");
}

function mapFeatureState(state) {
  if (state === "in_progress") return "active";
  if (state === "review") return "verify";
  if (state === "done") return "shipped";
  if (state === "blocked") return "blocked";
  return "ready";
}

function mapPrivateFeatureState(state) {
  if (state === "in_progress") return "active";
  if (state === "review") return "review";
  if (state === "done") return "done";
  if (state === "blocked") return "blocked";
  return "planned";
}

function mapLedgerState(state) {
  if (state === "in_progress") return "active";
  if (state === "review") return "review";
  if (state === "done") return "closed";
  if (state === "blocked") return "blocked";
  return "planned";
}

function uniqueArchiveDir(target) {
  const stamp = new Date().toISOString().replace(/[-:]/g, "").replace(".", "-");
  const base = `docs/01-GOVERNANCE/archive/generated-governance-tables/${stamp}`;
  for (let index = 0; index < 100; index += 1) {
    const candidate = index === 0 ? base : `${base}-${String(index).padStart(2, "0")}`;
    if (!fs.existsSync(path.join(target.projectRoot, candidate))) return candidate;
  }
  throw new Error("Unable to allocate a unique governance table archive directory");
}
